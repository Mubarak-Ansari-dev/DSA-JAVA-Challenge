// topic- Linked List

/*
 * 1. A linked list is a linear data structure where each element (node) contains a value and a reference (link) to the next node in the sequence.
 * 2. Unlike arrays, linked lists do not require contiguous memory allocation, allowing for dynamic memory usage and efficient insertions and deletions.
 * 3. Each node in a linked list typically contains two parts: the data (value) and a reference (pointer) to the next node.
 * 4. The first node in a linked list is called the head, and the last node points to null, indicating the end of the list.
 * 5. Linked lists can be singly linked (each node points to the next node) or doubly linked (each node points to both the next and previous nodes).
 * 6. Common operations on linked lists include insertion, deletion, traversal, and searching for elements.
 */